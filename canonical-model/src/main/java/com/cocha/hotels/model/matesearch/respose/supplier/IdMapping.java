public class IdMapping {
	
	private String idCocha;
	private String supplierEAN;
	private	String supplierSabre;
	private String supplierBooking;
	
	public String getIdCocha() {
		return idCocha;
	}
	public void setIdCocha(String idCocha) {
		this.idCocha = idCocha;
	}
	public String getSupplierBooking() {
		return supplierBooking;
	}
	public void setSupplierBooking(String supplierBooking) {
		this.supplierBooking = supplierBooking;
	}
	public String getSupplierEAN() {
		return supplierEAN;
	}
	public void setSupplierEAN(String supplierEAN) {
		this.supplierEAN = supplierEAN;
	}
	public String getSupplierSabre() {
		return supplierSabre;
	}
	public void setSupplierSabre(String supplierSabre) {
		this.supplierSabre = supplierSabre;
	}
}
